#ifndef _FADE_H_
#define _FADE_H_

//-----------------------------------------------
//
//  インクルードファイル
//
//-----------------------------------------------
#include "main.h"
#include "mode.h"

//-----------------------------------------------
//
//  定数定義 
//
//-----------------------------------------------
#define FADE_STRAT_R (0)
#define FADE_STRAT_G (0)
#define FADE_STRAT_B (0)

// 列挙型定義
typedef enum
{
	FADE_NONE = 0,
	FADE_IN,
	FADE_OUT,
	FADE_MAX
}FADE;

//-----------------------------------------------
//
//   関数プロトタイプ宣言
//
//-----------------------------------------------

//-----------------------------------------------
//
//　フェード初期化
//　
//-----------------------------------------------
void InitFade(void);

//-----------------------------------------------
//
//　フェードセットアップ
//　
//-----------------------------------------------
void SetupFade(void);

//-----------------------------------------------
//
//　フェード終了処理
//　
//-----------------------------------------------
void UninitFade(void);

//-----------------------------------------------
//
//　フェードイン関数
//　フェードインを指定色でスタートする
//  r,g,b　・・・　フェード色　
//　frame　・・・　フェード時間（フレーム数）
//　
//-----------------------------------------------
void FadeIn( float r, float g, float b, int frame );

//-----------------------------------------------
//
//　フェードアウト関数
//　フェードアウトを指定色でスタートする
//  r,g,b　・・・　フェード色　
//　frame　・・・　フェード時間（フレーム数）
//　
//-----------------------------------------------
void FadeOut( float r, float g, float b, int frame );

//-----------------------------------------------
//
//　フェード更新処理関数
//　
//-----------------------------------------------
void UpdateFade(void);

//-----------------------------------------------
//
//　フェード描画処理関数
//　
//-----------------------------------------------
void DrawFade(void);

//-----------------------------------------------
//
//　現在のフェード取得
//　
//-----------------------------------------------
FADE GetFade( void );

//-----------------------------------------------
//
//　フェードのセット
//　
//-----------------------------------------------
void SetFade( FADE fadeSet );

//-----------------------------------------------
//
//　フェードの位置セット
//　
//-----------------------------------------------
void SetFadePos( D3DXVECTOR3 pos, D3DXVECTOR3 rot );

#endif