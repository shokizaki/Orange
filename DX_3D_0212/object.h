#include "main.h"

//===============================================
//
// マクロ定義
//
//===============================================
#define MAX_OBJECT ( 50 )

//===============================================
//
// 構造体定義
//
//===============================================
typedef enum
{
	OBJECTTYPE_A = 0,
	OBJECTTYPE_B,
	OBJECTTYPE_C,
	OBJECTTYPE_D,
	OBJECTTYPE_E,
	OBJECTTYPE_MAX
}OBJECTTYPE;

typedef struct
{
	D3DXVECTOR3 pos;				// 位置
	D3DXVECTOR3 rot;				// 角度
	D3DXVECTOR3 scl;				// スケール

	D3DXVECTOR3 harfSize;			// 大きさ

	D3DXMATRIX mtxWorld;			// ワールドマトリックス

	LPD3DXMESH pMeshObject;			// メッシュ情報へのポインタ
	LPD3DXBUFFER pBuffMatObject;	// マテリアル情報へのポインタ
	DWORD numMatObject;				// マテリアルの数
	LPDIRECT3DTEXTURE9 pTextureObject;		// テクスチャへのポインタ
	
	OBJECTTYPE type;
	bool bUse;
}OBJECT;

//===============================================
//
//  関数プロトタイプ宣言
//
//===============================================

//===============================================
