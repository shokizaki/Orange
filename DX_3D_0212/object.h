//===============================================
//
// オブジェクトに関する終了処理
//
//===============================================
void UninitObject(void);	

//===============================================
//
// オブジェクトに関する更新処理
//
//===============================================
void UpdateObject(void);

//===============================================
//
// オブジェクトに関する描画処理
//
//===============================================
void DrawObject(void);

//===============================================
//
// オブジェクト構造体変数取得
//
//===============================================
void SetObject( D3DXVECTOR3 pos, OBJECTTYPE type );

//===============================================
//
// オブジェクト構造体変数取得
//
//===============================================
OBJECT *GetObject(void);

//===============================================
//
// 当たり判定の情報を取得
//
//===============================================
COL_RECT *GetRectObject();


#endif